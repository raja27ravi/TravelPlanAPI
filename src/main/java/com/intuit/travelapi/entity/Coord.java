package com.intuit.travelapi.entity;

public class Coord {
	 private String lon;
	 private String lat;


	 // Getter Methods 

	 public String getLon() {
	  return lon;
	 }

	 public String getLat() {
	  return lat;
	 }

	 // Setter Methods 

	 public void setLon(String lon) {
	  this.lon = lon;
	 }

	 public void setLat(String lat) {
	  this.lat = lat;
	 }
}
