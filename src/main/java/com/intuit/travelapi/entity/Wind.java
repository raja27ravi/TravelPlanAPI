package com.intuit.travelapi.entity;

public class Wind {
	private String speed;
	 private String deg;


	 // Getter Methods 

	 public String getSpeed() {
	  return speed;
	 }

	 public String getDeg() {
	  return deg;
	 }

	 // Setter Methods 

	 public void setSpeed(String speed) {
	  this.speed = speed;
	 }

	 public void setDeg(String deg) {
	  this.deg = deg;
	 }
}
